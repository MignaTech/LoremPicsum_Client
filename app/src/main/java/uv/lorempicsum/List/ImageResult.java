package uv.lorempicsum.List;

public class ImageResult {
    private int id;
    private String author;
    private int width;
    private int height;
    private String url;
    private String download_url;

    public int getId() {
        return id;
    }
    public String getAuthor() {
        return author;
    }
    public int getWidth() {
        return width;
    }
    public int getHeight() {
        return height;
    }
    public String getUrl() {
        return url;
    }
    public String getDownload_url() {
        return download_url;
    }
}

